<li>CPU主频=1/时钟周期</li>
</ul>
<h4 id="cpi-clock-cycle-per-instruction" tabindex="-1"><a class="header-anchor" href="#cpi-clock-cycle-per-instruction" aria-hidden="true">#</a> CPI(Clock Cycle Per Instruction)</h4>
<ul>
<li>CPI(Clock Cycle Per Instruction): 执行一条指令所需的时钟周期数, 不同的指令, CPI不同. 甚至相同的指令, CPI也有可能变化(在CPU内部实现方式不同)</li>
<li>执行一条指令消耗的时间=CPI*时钟周期</li>
<li>CPU执行时间(整个程序消耗的时间)=CPU时钟周期数/主频=(指令条数*CPI)/主频</li>
</ul>
<p>例如: 某CPU的主频为1000Hz, 某程序包含100条指令, 平均来看指令的CPI=3. 该程序在CPU上执行需要多长时间? 1000<em>3</em>1/1000=0.3s</p>
<h4 id="ips-instruction-per-second" tabindex="-1"><a class="header-anchor" href="#ips-instruction-per-second" aria-hidden="true">#</a> IPS(Instruction Per Second)</h4>
<ul>
<li>IPS(Instruction Per Second): 每秒执行多少条指令</li>
<li>IPS=主频/平均CPI</li>
</ul>
<h4 id="flops-floating-point-operations-per-second" tabindex="-1"><a class="header-anchor" href="#flops-floating-point-operations-per-second" aria-hidden="true">#</a> FLOPS(FLoating-point Operations Per Second)</h4>
<ul>
<li>FLOPS(FLoating-point Operations Per Second): 每秒执行多少次浮点运算</li>
</ul>
