<h3 id="一些思考" tabindex="-1"><a class="header-anchor" href="#一些思考" aria-hidden="true">#</a> 一些思考</h3>
<ul>
<li>主频高的CPU一定比主频低的CPU快吗?
<ul>
<li>不一定, 如两个CPU, A的主频是2GHz, 平均CPI=10; B的主频为1GHz, 平均CPI=1; 则A的IPS为0.2G, B的IPS为1G</li>
</ul>
</li>
<li>若上述A和B两个CPU的平均CPI相同, 则A一定更快吗?
<ul>
<li>不一定, 还要看指令系统, 如A不支持乘法指令, 只能用多次的加法实现惩乘法; 而B支持乘法指令</li>
